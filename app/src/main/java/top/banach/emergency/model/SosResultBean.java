package top.banach.emergency.model;

public class SosResultBean {
    private String id;
    private String code;
    private String msg;

    public void setId(String id) {
        this.id = id;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getId() {
        return id;
    }

    public String getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
